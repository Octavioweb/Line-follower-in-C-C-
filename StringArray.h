void Matrix (char *Buffer, char *message)
{
	unsigned char i, temp;
	for (char r=0; r<16;r++)
		*Buffer++=0;
	//*Buffer=1;
	while (*message!=0 && *message!=0x0d)
	{
		if (*message>=48 && *message<=57)
		{
			temp=*message-48;
			for (i=0;i<=5;i++)
			{
				*Buffer++=numero[temp][i];
			}
		}
		else if (*message>=65 && *message<=91)
		{
			temp=*message-65;
			for (i=0;i<=5;i++)
			{
				*Buffer++= mayuscula[temp][i];
			}
		}
		else if (*message>=97 && *message<=122)
		{
			temp=*message-97;
			for (i=0;i<=5;i++)
			{
				*Buffer++= minuscula[temp][i];
			}
		}
		else if (*message==32)
		{
			temp=*message-32;
			for (i=0;i<=5;i++)
			{
				*Buffer++= espacio[temp][i];
			}
		}
		
		message+=1;
	}
	
	for (int n = 0; n<16; n++) {// Se an'aden 128 casillas de NULL al inicio
		
		*Buffer++=0b00000000;}
	
	//for (char r=0; r<16;r++)
	//
	*Buffer++=0x0D;
		//*Buffer++=0;
}

